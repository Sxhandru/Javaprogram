package program;

public class Swapping {
	public static void main(String[] args) {
		int a=3,b=2,c;
		System.out.println("Original value");
		System.out.println(a);
		System.out.println(b);
		System.out.println("Swap Value");
		c=a;
		a=b;
		b=c;
		System.out.println(a);
		System.out.println(b);
	}

}
